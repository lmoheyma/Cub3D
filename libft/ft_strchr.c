#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (!s || c == '\0')
		return (0);
	while (*s != '\0' && *s != (unsigned char)c)
		s++;
	if (*s == (unsigned char)c)
		return ((char *)s);
	return (NULL);
}
